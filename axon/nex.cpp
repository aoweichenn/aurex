// Nex build tool for M0 projects. C++20, standalone, zero external deps.
//
// Usage:  nex                  # reads ./Nexfile, writes ./axbuild.mk
//         nex <Nexfile>       # specify config path
//         nex -f <Nexfile>    # same
//         make -f axbuild.mk   # then build

