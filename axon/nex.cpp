// ── CLI ───────────────────────────────────────────────────────────────────

int main(int argc, char** argv) {
    fs::path config_path = "Nexfile";
    fs::path output_path = "axbuild.mk";

    for (int i = 1; i < argc; ++i) {
        std::string_view arg(argv[i]);
        if (arg == "-h" || arg == "--help") {
            std::cout
                << "Nex — build tool for M0 projects\n"
                << "usage: nex [Nexfile] | -o <output>\n"
                << "       defaults to ./Nexfile, outputs ./axbuild.mk\n"
                << "       then: make -f axbuild.mk\n";
            return 0;
        }
        if (arg == "-o" && i + 1 < argc) {
            output_path = argv[++i];
        } else if (arg == "-f" && i + 1 < argc) {
            config_path = argv[++i];
        } else if (!arg.empty() && arg.front() != '-') {
            config_path = arg;
        } else {
            std::cerr << "nex: unknown flag: " << arg << '\n';
            return 2;
        }
    }

    auto cfg = read_config(config_path);
    if (!cfg) {
        std::cerr << "nex: failed to read " << config_path.string() << '\n';
        return 1;
    }

    if (!validate_config(*cfg)) return 1;

    // Print scan summary
    auto source_text = read_file(cfg->entry);
    if (source_text) {
        auto info = scan_source(*source_text);
        std::cout << "nex: module   " << info.module_name << '\n';
        for (auto& imp : info.imports) {
            std::cout << "nex:   import " << imp << '\n';
        }
    }

    // Write Makefile
    std::ofstream out(output_path, std::ios::binary);
    if (!out) {
        std::cerr << "nex: failed to open " << output_path.string() << '\n';
        return 1;
    }
    emit_makefile(out, *cfg);
    out.close();

    if (!out) {
        std::cerr << "nex: write error\n";
        return 1;
    }

    std::cout << "nex: generated " << output_path.string() << '\n';
    return 0;
}

